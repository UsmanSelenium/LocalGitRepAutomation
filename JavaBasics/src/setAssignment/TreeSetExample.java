package setAssignment;

import java.util.TreeSet;

public class TreeSetExample {
	public static void main(String args[]) {
		
		TreeSet<String> tset = new TreeSet<String>();
		
		tset.add("Mango");
		tset.add("Banana");
		tset.add("Grape");
		tset.add("Apple");
		System.out.println("Elements in TreeSet: "+tset);
	}

}
